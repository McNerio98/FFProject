package com.ferreteria.entidad;

import com.ferreteria.anotaciones.*;

/**
 *
 * @author ZEUS
 */
@Entity(table="Roles")
public class Rol {
    @PrimaryKey
    @AutoIncrement
    private Integer idRol;
    
    @NotNull
    @FieldName(name="Rol")
    private String rol;

    public Rol() {
    }

    public Rol(Integer idRol, String rol) {
        this.idRol = idRol;
        this.rol = rol;
    }

    public Integer getIdRol() {
        return idRol;
    }

    public void setIdRol(Integer idRol) {
        this.idRol = idRol;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }
    
    
    

}
